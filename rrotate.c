#include "libpushswap.h"

void		ft_rrotate(t_sort **a)
{
	if (*a && *a != a[1])
	{
		a[1]->next = *a;
		(*a)->prev = a[1];
		*a = a[1];
		a[1] = (*a)->prev;
		a[1]->next = 0;
		(*a)->prev = 0;
	}
}
